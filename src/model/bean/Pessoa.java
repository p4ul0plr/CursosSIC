package model.bean;

import java.sql.Date;

/**
 *
 * @author paulo
 */

public abstract class Pessoa {
    
    protected int cpf;
    protected String nome;
    protected String sexo;
    protected String email;
    protected Date dataNascimento;
    protected int telDdd;
    protected int telNumero;
    protected String endRua;
    protected String endNumero;
    protected String endBairro;
    protected String endCidade;
    protected String endEstado;

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
    
    

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
        public Date getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(Date dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public int getTelDdd() {
        return telDdd;
    }

    public void setTelDdd(int telDdd) {
        this.telDdd = telDdd;
    }

    public int getTelNumero() {
        return telNumero;
    }

    public void setTelNumero(int telNumero) {
        this.telNumero = telNumero;
    }

    public String getEndRua() {
        return endRua;
    }

    public void setEndRua(String endRua) {
        this.endRua = endRua;
    }

    public String getEndNumero() {
        return endNumero;
    }

    public void setEndNumero(String endNumero) {
        this.endNumero = endNumero;
    }

    public String getEndBairro() {
        return endBairro;
    }

    public void setEndBairro(String endBairro) {
        this.endBairro = endBairro;
    }

    public String getEndCidade() {
        return endCidade;
    }

    public void setEndCidade(String endCidade) {
        this.endCidade = endCidade;
    }

    public String getEndEstado() {
        return endEstado;
    }

    public void setEndEstado(String endEstado) {
        this.endEstado = endEstado;
    }
    
    
    
}
